export type MessageKey = keyof typeof koMessages;
export type Locale = 'ko' | 'en';

export const messages: Record<Locale, Record<MessageKey, string>> = {
  ko: koMessages,
  en: enMessages,
};

export const DEFAULT_LOCALE: Locale = 'ko';

/**
 * Resolve a message for a locale with optional `{var}` interpolation.
 * Falls back to the key itself if missing (never throws), so a missing
 * translation is visible but non-fatal.
 */
export function translate(
  locale: Locale,
  key: MessageKey,
  vars?: Record<string, string | number>,
): string {
  const table = messages[locale] ?? messages[DEFAULT_LOCALE];
  let text: string = table[key] ?? messages[DEFAULT_LOCALE][key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}
